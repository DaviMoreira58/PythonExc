n = input('Digite algo ')
print('\033[30;1mO tipo primitivo é', type(n))
print('\033[31;1mÉ um numero?', n.isalnum())
print('\033[32;1mÉ alfanumerico?', n.isalnum())
print('\033[33;1mÉ decimal?', n.isdecimal())
print('\033[34;1mÉ possivel escrever?', n.isprintable())
print('\033[35;1mEstá em minusculo', n.islower())
print('\033[36;1mEstá em maiusculo?', n.isupper())
print('\033[37;1mEstá capitalizada?', n.istitle())
